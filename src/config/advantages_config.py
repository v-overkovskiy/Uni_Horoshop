# Конфигурация для валидации
VALIDATION_CONFIG = {
    'ru': {
        'stop_phrases': RU_STOP_PHRASES,
        'domain_tokens': RU_DOMAIN_TOKENS,
        'min_length': 24,
        'max_length': 160,
        'min_advantages': 2,
        'max_advantages': 4
    },
    'ua': {
        'stop_phrases': UA_STOP_PHRASES,
        'domain_tokens': UA_DOMAIN_TOKENS,
        'min_length': 24,
        'max_length': 160,
        'min_advantages': 2,
        'max_advantages': 4
    }
}

def get_config(locale: str) -> Dict:
    """Получить конфигурацию для локали"""
    return VALIDATION_CONFIG.get(locale, VALIDATION_CONFIG['ru'])

def is_domain_relevant(text: str, locale: str) -> bool:
    """Проверить, содержит ли текст доменные токены"""
    config = get_config(locale)
    text_lower = text.lower()
    
    # Проверяем наличие доменных токенов
    for token in config['domain_tokens']:
        if token in text_lower:
            return True
    
    # Проверяем наличие цифр и единиц измерения
    import re
    if re.search(r'\d+', text) or re.search(r'(мл|вт|°c|градус|мг|г|кг|см|мм|м)', text_lower):
        return True
    
    return False

def contains_stop_phrase(text: str, locale: str) -> bool:
    """Проверить, содержит ли текст стоп-фразы"""
    config = get_config(locale)
    text_lower = text.lower()
    
    for phrase in config['stop_phrases']:
        if phrase in text_lower:
            return True
    
    return False


